<h3>A way to build responsive design with ease. Let's find out why.</h3>


<p>When thinking about implementing responsive design on your website, you will find that there are many different approaches and things to consider. It can be overwhelming to get started learning about them and decide to pick one or a couple. This article will help you start with flexbox because it’s known to be an easy and yet, powerful way to build responsive design in these recent years. The article will address the four reasons you should use flexbox.</p>

<h4><strong>Reasons to use flexbox</strong></h4>
    
<p><strong>It’s well supported in most browsers.</strong> According to Ire Aderinokun, a frontend developer and user interface designer, <em>‘The main reason I hadn’t been using flexbox was because of a perceived lack of browser support. But in actual fact, flexbox is very well supported, at 95.89% global support. If you disregard IE 10 and below, which Microsoft says you now can, this number is even higher.’</em> (Aderinokun)  Knowing about this will make you less worried about whether or not your effort in using flexbox would work in your users’ browsers.</p>
 
<p><strong>It’s simple to start.</strong> Although there are several properties and values to style flexbox, you can start by simply using a few properties such as, display, justify-content, and align-item. When you use display: flex; with a container, then it becomes a flexbox. justify-content property will allow you to style the elements inside the container horizontally. And align-items enables you to style the elements vertically.</p>

<p><strong>It’s a better solution than floating elements.</strong> When floating elements, we disrupt the flow of elements and have to do more work to clear the messy flow. Using flexbox allows us to manage inline elements easier and more efficiently by using justify-content property. This property has a value such as, space-evenly, that distributes space evenly between each element.</p>

<p><strong>It’s great for scaling. </strong>: Alex Caldwell, the Creative Director for Brolik, a Philadelphia-based digital agency, mentions, <em>‘Matching heights vertically used to be somewhat difficult and would require jQuery after the page loaded. Now flexbox handles the vertical height and floats by default. It’s one of my favorite features of flexbox because I love orderly grids. The align-items property in flexbox defaults to “stretch,” which leads me into my favorite feature of flexbox.’</em>(Caldwell) You can see his visual example down below.</p>

